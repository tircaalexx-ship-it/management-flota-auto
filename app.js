// Login API - CORECTAT
app.post('/api/login', (req, res) => {
    const { username, password } = req.body;
    
    console.log('üîê √éncercare login pentru:', username);
    
    if (!username || !password) {
        return res.status(400).json({ error: 'Username »ôi parolƒÉ sunt obligatorii' });
    }
    
    db.get('SELECT * FROM users WHERE username = ? AND status = "activ"', [username], (err, user) => {
        if (err) {
            console.error('‚ùå Eroare baza de date login:', err);
            return res.status(500).json({ error: 'Eroare server' });
        }
        
        if (!user) {
            console.log('‚ùå Utilizator negƒÉsit:', username);
            return res.status(401).json({ error: 'Utilizator sau parolƒÉ incorectƒÉ' });
        }
        
        if (bcrypt.compareSync(password, user.password_hash)) {
            // SeteazƒÉ sesiunea
            req.session.user = {
                id: user.id,
                username: user.username,
                nume: user.nume,
                is_admin: user.is_admin
            };
            
            console.log('‚úÖ Login succes pentru:', username, 'Sesiune:', req.session.user);
            
            // SalveazƒÉ sesiunea
            req.session.save((err) => {
                if (err) {
                    console.error('‚ùå Eroare salvare sesiune:', err);
                    return res.status(500).json({ error: 'Eroare sesiune' });
                }
                console.log('‚úÖ Sesiune salvatƒÉ pentru:', username);
                res.json({ success: true, message: 'Autentificare reu»ôitƒÉ!' });
            });
        } else {
            console.log('‚ùå ParolƒÉ incorectƒÉ pentru:', username);
            res.status(401).json({ error: 'Utilizator sau parolƒÉ incorectƒÉ' });
        }
    });
});

